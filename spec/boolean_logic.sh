describe 'Boolean And'

expect '1 && 1' '1'
expect '1 && 0' '0'
expect '0 && 1' '0'
expect '0 && 0' '0'
expect '1 && 1 && 1' '1'
expect '1 && 1 && 0' '0'
expect '1 && 0 && 1' '0'
expect '1 && 0 && 0' '0'
expect '0 && 1 && 1' '0'
expect '0 && 1 && 0' '0'
expect '0 && 0 && 1' '0'
expect '0 && 0 && 0' '0'
expect '1 && 1 && 1 && 1' '1'


describe 'Boolean Or'

expect '1 || 1' '1'
expect '1 || 0' '1'
expect '0 || 1' '1'
expect '0 || 0' '0'
expect '1 || 1 || 1' '1'
expect '1 || 1 || 0' '1'
expect '1 || 0 || 1' '1'
expect '1 || 0 || 0' '1'
expect '0 || 1 || 1' '1'
expect '0 || 1 || 0' '1'
expect '0 || 0 || 1' '1'
expect '0 || 0 || 0' '0'
expect '1 || 1 || 1 || 1' '1'


describe 'Boolean Not'

expect '!1' '0'
expect '!0' '1'
expect '!!1' '1'
expect '!!0' '0'
expect '!(1 && 1)' '0'
expect '!(1 && 0)' '1'
expect '!(0 && 1)' '1'